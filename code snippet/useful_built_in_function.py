# -----------------------------------------------------------------------------
# 最大値
# -----------------------------------------------------------------------------
print(max([10, 42, 59, 31, 22])) # → 59


# -----------------------------------------------------------------------------
# 最小値
# -----------------------------------------------------------------------------
print(min([10, 42, 59, 31, 22])) # → 10


# -----------------------------------------------------------------------------
#　全ての要素がTrueか判断
# -----------------------------------------------------------------------------
S = [10, 20, 30]
print(all(i % 10 == 0 for i in S)) # → True


# -----------------------------------------------------------------------------
# リストを降順でソート
# -----------------------------------------------------------------------------
a = [1, 3, 2]
b = sorted(a, reverse=True) 
print(b) # → [3, 2, 1]


# -----------------------------------------------------------------------------
# リストの要素をカウント
# -----------------------------------------------------------------------------
l = ['a', 'a', 'a', 'a', 'b', 'c', 'c']
print(l.count('a')) # → 4


# -----------------------------------------------------------------------------
# 合計
# -----------------------------------------------------------------------------
sample_list = [1, 2, 3]
print(sample_list) # → 6


